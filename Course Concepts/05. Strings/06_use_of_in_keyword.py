#       Use of "in" keyword in Python Strings
#           This keyword can be used to check whether the left 
#   substring is present in the string or not.
#       The return type of the operation will be a boolean value. 
#   If the substring is present, it will return "True" else "False".

name = "Tejas"

print('T' in name) # T is present in the string
print("n" in name)  # n is absent in the string

print("Tej" in name)  # the substring "tej" is present in the name string