#concept of indexing of character is used in Accessing the elements of the string

#Positive Indexing
val1 = "Tejas"
print(val1[0])
print(val1[1])
print(val1[2])
print(val1[3])
print(val1[4])

#Negative Indexing
val1 = "Tejas"
print(val1[-2])
print(val1[-4])
print(val1[-5])
print(val1[-3])
print(val1[-1])

# Accessing value more than the index values present
print(val1[5])
# results "Index out of range error"
