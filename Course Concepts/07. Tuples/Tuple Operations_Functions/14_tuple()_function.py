# Use of tuple() function

# This function is used to convert a given iterable into tuple



# Case 1 : When the elements are the integers

list1 = [1,2,3,4,5]
print(list1)

tuple1 = tuple(list1)
print(tuple1)


# Case 2 : When the element is a string value

string1 = "Tejas"
print(string1)

tuple2 = tuple(string1)
print(tuple2)