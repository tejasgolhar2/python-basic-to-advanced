# dictionaries are mutable

# INSERTING AN ELEMENT / NEW ELEMENT IN A DICTIONARY

dict1 = {}
dict1["Tejas"] = "He is a very good boy that lives in Wardha"
dict1["Ram"] = "the inclusion of the whole universe"
print(dict1)

# Updating the value of pre-defined key in dictionary
dict1["Tejas"] = "He is a boy doing engineering in Black Gold City"

print(dict1)