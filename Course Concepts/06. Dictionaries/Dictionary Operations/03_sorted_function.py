# Use of Sorted function in Python Dictionaries

# the function returns a "list of keys present" in the dictionary "in the sorted manner"

random_dict = {
    1 :'one',
    3 :'three',
    2 :'two',
    6 :'six',
    4 :'four',
    8 :'eight',
    7 :'seven',
    5 :'five',
}

print(sorted(random_dict))
