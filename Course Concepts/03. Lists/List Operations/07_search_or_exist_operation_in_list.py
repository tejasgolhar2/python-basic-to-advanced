# Use of IN and NOT IN Operator in Python

# the operator results boolean value
# if the intended element exists in the list, True is returned
# if the intended element doesn't exists in the list, False is returned

fruits = ["apple", "banana", "orange"]

# search for kiwi in the list of fruits

print("kiwi" in fruits)

# search for apple in the list

print("apple" in fruits)

# get truth value for a value not to be present in the list
print("Pineapple" not in fruits)
