# the sort method in python is used to sort the given list in a specified order
# the 'Sort' method returns NONE

# by default the given list is sorted in an ascending order
objects = ['pen','pencil','eraser','bag','mouse','biscuit','potato']
objects.sort()
print(objects)

# to sort the given list in descending order
objects.sort(reverse=True)
print(objects)


