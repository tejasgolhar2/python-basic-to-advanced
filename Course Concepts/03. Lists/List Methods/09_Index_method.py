# INDEX METHOD 

# to search for the index value of an element present in the list

names = ["Neha","Ravi","Shashikant","Rubina","Ahemad","Jatin","John"]

print(names.index("Ravi"))

# to search for the index value of an element in the list from a given index location of the list

print(names.index("Ahemad",2))

print(names.index("Rubina",2,5))