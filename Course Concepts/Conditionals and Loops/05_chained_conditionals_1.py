num1 = int(input("Enter first number : \n"))
num2 = int(input("Enter second number : \n"))

if num1 > num2:
    print("The value of first number is greater than second")

elif num1 < num2:
    print("The value of first number is lesser than second")

else:
    print("Both numbers are equal to each other")

